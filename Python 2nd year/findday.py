day = int(input('Enter the number of day: '))
if day == 1:
    print("Day is Monday")
elif day == 2:
    print("Day is Tuesday")
elif day == 3:
    print("Day is Wednesday")
elif day == 4:
    print ("Day is Thursday")
elif day == 5:
    print ("Day is Friday")
elif day == 6:
    print ("Day is Saturday")
elif day == 7:
    print ("Day is Sunday")
else:
    print("Input a Correct Day Number There are only 7Days per week!")